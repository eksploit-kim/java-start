package variable;

public class Var2 {
    public static void main(String[] args) {
        int a; // 변수 선언
        a = 20; // 10 -> 20 변수 초기화

        System.out.println(a); // -> sout
        System.out.println(a);
        System.out.println(a);
        // 참고로 변수의 값은 반복해서 읽을 수 있다. 변수의 값을 읽는다고 값이 없어지는 것이 아니다.
    }
}

// a의 값을 변경하면 출력 결과가 모두 함께 변경되는 것을 확인할 수 있다.
